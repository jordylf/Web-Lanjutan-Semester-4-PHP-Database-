<?php
    // Angka statis
    $angka1 = 10;
    $angka2 = 5;

    echo "OPERASI PENJUMLAHAN/PERHITUNGAN<br>";

    // Penjumlahan
    $penjumlahan = $angka1 + $angka2;
    echo "Penjumlahan: $penjumlahan <br>";

    // Pengurangan
    $pengurangan = $angka1 - $angka2;
    echo "Pengurangan: $pengurangan <br>";

    // Perkalian
    $perkalian = $angka1 * $angka2;
    echo "Perkalian: $perkalian <br>";

    // Pembagian
    $pembagian = $angka1 / $angka2;
    echo "Pembagian: $pembagian <br>";

    // Kuadrat
    $kuadrat = $angka1 ** 2;
    echo "Kuadrat: $kuadrat <br>";

    // Akar
    $akar_angka2 = sqrt($angka2);
    echo "Akar: $akar_angka2 <br>";
?>